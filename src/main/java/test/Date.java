package test;

import java.util.regex.Pattern;

public class Date {
	public static void main(String[] args) {
		valit("2021-02-03");
		valit("2021/02/03");
		valit("2031年05月03日");
		valit("2021.02.03");
		valit("2021_02_03");
	}

	private static void valit(String dateS) {
		String dateEL = "((^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(10|12|0?[13578])([-\\/\\._])(3[01]|[12][0-9]|0?[1-9])$)|(^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(11|0?[469])([-\\/\\._])(30|[12][0-9]|0?[1-9])$)|(^((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(0?2)([-\\/\\._])(2[0-8]|1[0-9]|0?[1-9])$)|(^([2468][048]00)([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([3579][26]00)([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([1][89][0][48])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([2-9][0-9][0][48])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([1][89][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([2-9][0-9][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([1][89][13579][26])([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([2-9][0-9][13579][26])([-\\/\\._])(0?2)([-\\/\\._])(29)$))";
		Pattern p = Pattern.compile(dateEL);
		System.out.print(p.matcher(dateS).matches()+":");
		
		String el2="^(\\d{4}年\\d{1,2}月\\d{1,2}日$)$";
		Pattern p2 = Pattern.compile(el2);
		System.out.println(p2.matcher(dateS).matches());
	}
}
